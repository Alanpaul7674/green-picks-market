export const getProductById = (id: number): Product | undefined => {
  return products.find(product => product.id === id);
};

export const getRelatedProducts = (product: Product, limit: number = 6): Product[] => {
  return products
    .filter(p => p.id !== product.id && (p.category === product.category || p.brand === product.brand))
    .sort(() => Math.random() - 0.5)
    .slice(0, limit);
};

export const getFeaturedProducts = (limit: number = 4): Product[] => {
  return products
    .filter(p => p.isSustainable || p.isNew)
    .sort(() => Math.random() - 0.5)
    .slice(0, limit);
};

export const getNewArrivals = (limit: number = 4): Product[] => {
  return products
    .filter(p => p.isNew)
    .sort(() => Math.random() - 0.5)
    .slice(0, limit);
};

export const getProductsByCategory = (category: string, limit?: number): Product[] => {
  const filteredProducts = products.filter(p => p.category === category);
  return limit ? filteredProducts.slice(0, limit) : filteredProducts;
};
